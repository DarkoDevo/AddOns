-- Create a tab to show a table

function GUI.DebugLog_EnableEvents(tab, vtab)
	if not GUI.DebugLog_IsFormatRegistered("events") then
		GUI.DebugLog_RegisterFormat("events", private.events)
	end

	if WOW_PROJECT_ID == 1 then
		vtab = vtab or private.eventstrings_retail
		addon:DebugLog("WARN~Events for Retail (ID " ..  WOW_PROJECT_ID .. ") loaded")
	else
		vtab = vtab or private.eventstrings_classic
		addon:DebugLog("WARN~Events for Classic (ID " ..  WOW_PROJECT_ID .. ") loaded")
	end

	-- create tab name
	local a
	if type(tab) == "string" and tab ~= "" and tab ~= "nil" then
		a = tab
	else
		a = tostring(vtab)
	end

	-- ensure tab name is unique
	while GUI.guiTabs.timer[a] do
		a = a .. "*"
	end

	private.DebugPrintf("OK~DebugLog_EnableEvents(%s, %s) -> (%s, %s)", tostring(tab), tostring(vtab), tostring(a), tostring(vtab))

	-- Event Handlers
	local eventHandlers = {
		OnEnter = function(btn, data)
				if btn.btnCnt then
					GUI:SetStatusLine(GUI.container.titleStatusText[btn.btnCnt])
				else
					if type(data) == "table" and data.en then
						GUI:SetStatusLine("Click to show table in new tab. Right-click to show parent table.")
					end

					GameTooltip:SetOwner(btn, "ANCHOR_TOPLEFT")
					GameTooltip:ClearLines()
					for i, n in ipairs(data.data) do
						if i > 1 then
							if n[2] and n[2] ~= "" then
								GameTooltip:AddDoubleLine(private.events.colNames[i], "|cffffffff" .. tostring(n[1]) .. "|r")
							end
						end
					end
					GameTooltip:Show()
				end
			end,
		OnLeave = function(btn, data)
			if btn.btnCnt then
				GUI:SetStatusLine()
			end
			if type(data) == "table" then
				GameTooltip:Hide()
				if BattlePetTooltip and BattlePetTooltip.Hide then
					BattlePetTooltip:Hide()
				end
				GUI:SetStatusLine()
			end
		end,
		OnClick = function(btn, data, button)
			private.DebugPrintf("  OnClick(%s, %s, %s), data.en=%s", tostring(btn), tostring(data), tostring(button), tostring(data.en))
			if not data then return end
			if button == "LeftButton" then
				if data.en and data.en[20] then
					-- EVENTFRAME = data.en[20]
					-- peruse in new table tab
					addon.Tables:Load(data.en[3] .. " / " .. (data.en[5] or ""), data.en[20])
				end
			end
			if button == "RightButton" then
				if data.en and data.en[20] and type(data.en[20]) == "table" then
					local parent = private.tryCall(data.en[20].GetParent, data.en[20])
					if type(parent) == "table" then
						local name = private.tryCall(parent.GetName, parent) or ""
						local addr = strmatch(tostring(data.en[20]), "table: ([0-9A-Z]+)") or ""
						-- EVENTFRAME = parent of data.en[20]
						-- peruse in new table tab
						addon.Tables:Load(addr .. " / " .. name, parent)
					end
				end
			end
		end,
	}

	local fu = {}
	fu[a] = {}
	fu[a].select = function()
		GUI.debugLog_colCtrl[a].w:SetEventHandlers(eventHandlers)
	end

	GUI.DebugLog_CreateTab(a, "events", fu)

	if addon.DebugLog then
		addon:DebugLog("WARN~Log for '" .. a .. "' created")
	end

	addon.debuglog[a] = addon.debuglog[a] or {}
	for _, name in pairs(vtab) do
		local frames = { GetFramesRegisteredForEvent( name ) }

		-- addon:DebugLog(" %s: %s", tostring(#frames), name)
		if #frames == 0 then
			local en = {}
			en[2] = name
			tinsert(addon.debuglog[a], en)
		else
			for _, v in pairs(frames) do
				local en = {}
				en[2] = name
				if v and type(v) == "table" then
					en[20] = v -- saved for OnClick handler
				end
				en[3] = strmatch(tostring(v), "table: ([0-9A-Z]+)") or ""
				en[4] = private.tryCall("GetObjectType", v) or ""
				en[5] = private.tryCall("GetName", v) or ""
				en[6] = strmatch(tostring(private.tryCall("GetParent", v)) or "", "table: ([0-9A-Z]+)") or ""
				en[7] = ""
				local l = tostring(private.tryCall("IsShown", v))
				if l == "true" then
					en[7] = en[7] .. "* "
				end
				l = tostring(private.tryCall("GetNumChildren", v))
				if l ~= "0" then
					en[7] = en[7] .. "Ch=" .. l .. " "
				end
				-- 12.x: calls may return secret strings
				if (strmatch(en[5], "^PetFrame")) then
					en[5] = en[5]
				else
					l = tostring(private.tryCall("GetHeight", v))
					if issecretvalue(l) then
						en[7] = en[7] .. "H=(?) "
					else
						if l ~= "0" then
							en[7] = en[7] .. "H=" .. l .. " "
						end
					end
					l = tostring(private.tryCall("GetWidth", v))
					if issecretvalue(l) then
						en[7] = en[7] .. "W=(?) "
					else
						if l ~= "0" then
							en[7] = en[7] .. "W=" .. l .. " "
						end
					end
				end
				l = tostring(private.tryCall("GetPoint", v))
				if issecretvalue(l) then
					en[7] = en[7] .. "P=(?) "
				else
					if l ~= "nil" then
						en[7] = en[7] .. "P=" .. l .. " "
					end
				end
				l = tostring(private.tryCall("GetNumPoints", v))
				if issecretvalue(l) then
					en[7] = en[7] .. "P=(?) "
				else
					if l ~= "0" then
						en[7] = en[7] .. "NP=" .. l .. " "
					end
				end
				tinsert(addon.debuglog[a], en)
			end
		end
	end

	GUI:Load(a)
end

function private.tryCall(func, v)
	if not func or type(v) ~= "table" then
		return ""
	end

	-- 12.x: check for secrets
	if issecretvalue(v) then
		return "?! <SECRET>"
	end

	-- not safe to index it
	local mt = getmetatable(v)
	if mt and type(mt) == "table" and type(mt.__index) == "function" then
		return "?! metatable"
	end

	-- dont't call from insecure
	if v.IsForbidden then
		local status, res = pcall(v.IsForbidden, v)
		if status then
			if res then
				return "?! IsForbidden!"
			end
		else
			return "?! Error calling IsForbidden"
		end
	end

	local f = func
	if (type(f) ~= "table") then
		f = v[f]
	end

	-- has function
	if not f or type(f) ~= "function" then
		return ""
	end

	local status, res = pcall(f, v)
	if status then
		return res
	else
		return "?! " .. tostring(res)
	end
end

